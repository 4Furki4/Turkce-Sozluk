import {
  type MorphCategory,
  type MorphemeDefinition,
  type MorphemeSlot,
  type PartOfSpeech,
  type RuleId,
} from "./types";

type MorphemeSeed = {
  id: string;
  labelKey: string;
  preview: string;
  legacySuffixId?: string;
  realizationPattern: string;
  phonologyTriggers?: RuleId[];
  requires?: MorphemeDefinition["requires"];
  blocks?: MorphemeDefinition["blocks"];
  setsFeatures?: MorphemeDefinition["setsFeatures"];
};

function createInflectionMorpheme(
  base: MorphemeSeed & {
    category: MorphemeDefinition["category"];
    slot: MorphemeSlot;
    pos: PartOfSpeech;
    sourceCategories?: MorphCategory[];
    targetCategory?: MorphCategory;
    group: string;
    hidden?: boolean;
  },
): MorphemeDefinition {
  return {
    id: base.id,
    kind: "inflectional",
    category: base.category,
    slot: base.slot,
    sourcePos: base.pos,
    targetPos: base.pos,
    sourceCategories:
      base.sourceCategories ??
      (base.pos === "Noun"
        ? ["Noun", "VerbalNoun", "Participle"]
        : ["Verb"]),
    targetCategory: base.targetCategory ?? (base.pos as MorphCategory),
    group: base.group,
    labelKey: base.labelKey,
    preview: base.preview,
    hidden: base.hidden,
    legacySuffixId: base.legacySuffixId,
    requires: base.requires,
    blocks: base.blocks,
    realizationPattern: base.realizationPattern,
    phonologyTriggers: base.phonologyTriggers ?? [],
    setsFeatures: base.setsFeatures ?? {},
  };
}

function createDerivationalMorpheme(
  base: MorphemeSeed & {
    sourcePos: PartOfSpeech;
    targetPos: PartOfSpeech;
    sourceCategories?: MorphCategory[];
    targetCategory?: MorphCategory;
    group: string;
  },
): MorphemeDefinition {
  return {
    id: base.id,
    kind: "derivational",
    category: "derivation",
    slot: "derivation",
    sourcePos: base.sourcePos,
    targetPos: base.targetPos,
    sourceCategories: base.sourceCategories ?? [base.sourcePos as MorphCategory],
    targetCategory: base.targetCategory ?? (base.targetPos as MorphCategory),
    group: base.group,
    labelKey: base.labelKey,
    preview: base.preview,
    legacySuffixId: base.legacySuffixId,
    requires: base.requires,
    blocks: base.blocks,
    realizationPattern: base.realizationPattern,
    phonologyTriggers: base.phonologyTriggers ?? [],
    setsFeatures: {},
  };
}

function createNonfiniteMorpheme(
  base: MorphemeSeed & {
    targetPos: PartOfSpeech;
    targetCategory: MorphCategory;
    group: string;
  },
): MorphemeDefinition {
  return {
    id: base.id,
    kind: "nonfinite",
    category: "nonfinite",
    slot: "verb_nonfinite",
    sourcePos: "Verb",
    targetPos: base.targetPos,
    sourceCategories: ["Verb"],
    targetCategory: base.targetCategory,
    group: base.group,
    labelKey: base.labelKey,
    preview: base.preview,
    legacySuffixId: base.legacySuffixId,
    requires: base.requires,
    blocks: base.blocks,
    realizationPattern: base.realizationPattern,
    phonologyTriggers: base.phonologyTriggers ?? [],
    setsFeatures: {},
  };
}

